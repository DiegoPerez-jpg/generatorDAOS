

public class Tcentr{
private int numce;
private String nomce;
private String sence;

public Tcentr( int numce, String nomce, String sence )  {
this.numce = numce;
this.nomce = nomce;
this.sence = sence;
}

public int getNumce() {
    return numce;
}
 
public void setNumce(int numce) {
    this.numce = numce;
}
 
public String getNomce() {
    return nomce;
}
 
public void setNomce(String nomce) {
    this.nomce = nomce;
}
 
public String getSence() {
    return sence;
}
 
public void setSence(String sence) {
    this.sence = sence;
}
 


}